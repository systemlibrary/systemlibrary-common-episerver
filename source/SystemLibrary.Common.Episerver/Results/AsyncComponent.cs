using EPiServer.Core;
using EPiServer.Web.Mvc;

using Microsoft.AspNetCore.Html;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.ViewComponents;

using SystemLibrary.Common.Episerver.Extensions;
using SystemLibrary.Common.Web;

namespace SystemLibrary.Common.Episerver.Components;

/// <summary>
/// Inherit AsyncComponent to return Result, ResultAsync, or ReactServerSideResult or ReactServerSideResultAsync
/// </summary>
/// <example>
/// <code>
/// public class CarComponent : AsyncComponent<CamelCase>
/// {
///     protected override async Task&lt;IViewComponentResult&gt; InvokeComponentAsync(Car current)
///     {
///         return await ReactServerSideResultAsync(current, camelCaseProps: true);
///     }
/// }
/// </code>
/// </example>
public abstract class AsyncComponent<T> : AsyncBlockComponent<T> where T : BlockData
{
    const string WindowReactComponentsPath = "reactComponents";

    /// <summary>
    /// Returns a view component (file, cshtml) based on standards
    /// <para>Assumes ~/Content/Components/Car/Index.cshtml structure</para>
    /// <para>Fallback to ~/Content/Blocks/CarBlock/Index.cshtml if component contains "Block" in its type name (epi 11 compatibility...)</para>
    /// </summary>
    /// <returns>Return view component (view file) based on input</returns>
    protected async Task<IViewComponentResult> ResultAsync(object component, string viewName = "Index.cshtml")
    {
        if (viewName[0] != '~')
        {
            var componentName = component.GetType().Name;

            var componentType = componentName
                .Replace("Proxy", "")
                .Replace("ViewModel", "")
                .Replace("Model", "");

            if(componentName.Contains("Block", StringComparison.Ordinal))
                viewName = "~/Content/Blocks/" + componentType + "/" + viewName;
            else
                viewName = "~/Content/Components/" + componentType + "/" + viewName;
        }

        if (!viewName.EndsWith(".cshtml", StringComparison.Ordinal))
        {
            if (viewName.EndsWith("/", StringComparison.Ordinal))
                return await Task.FromResult(View(viewName + "Index.cshtml", component));
            else
                return await Task.FromResult(View(viewName + ".cshtml", component));
        }

        return await Task.FromResult(View(viewName, component));
    }

    /// <summary>
    /// Returns a view component (file, cshtml) based on standards
    /// <para>Assumes ~/Content/Components/Car/Index.cshtml structure</para>
    /// <para>Fallback to ~/Content/Blocks/CarBlock/Index.cshtml if component contains "Block" in its type name (epi 11 compatibility...)</para>
    /// </summary>
    /// <returns>Return view component (view file) based on input</returns>
    protected IViewComponentResult Result(object component, string viewName = "Index.cshtml")
    {
        if (viewName[0] != '~')
        {
            var componentType = component.GetType().Name.Replace("ViewModel", "").Replace("Model", "");

            viewName = "~/Content/Blocks/" + componentType + "/" + viewName;
        }

        if (!viewName.EndsWith(".cshtml", StringComparison.Ordinal))
        {
            if (viewName.EndsWith("/", StringComparison.Ordinal))
                return View(viewName + "Index.cshtml", component);
            else
                return View(viewName + ".cshtml", component);
        }

        return View(viewName, component);
    }

    /// <summary>
    /// Returns a react rendered view component
    /// <para>Assumes no cshtml files as it the engine returns HTML generated by the react component</para>
    /// </summary>
    /// <remarks>
    /// Assumes by default that all frontend react components is registered under DOM variable "window.reactComponents."
    /// <para>Use the 'componentFullName' if your react components live elsewhere</para>
    /// <para>Use 'additionalProps' as a mean to add more properties without extending 'model'</para>
    /// <para>Use 'additionalProps' as a mean to render a content area and its children as 'json object' instead of invoking the render method on the component. Supports up to a depth of 12 nested blocks</para>
    /// </remarks>
    /// <param name="model">Current block or view model you want as props to your component</param>
    /// <param name="additionalProps">Add additional props without extending 'model'. Tip, want content area rendered as 'json object' instead of pre-rendered as a string? Use .SelectFiltered on the ContentArea and add the same name here</param>
    /// <param name="camelCaseProps">Force all properties to be camel cased</param>
    /// <param name="renderClientOnly">Render client only, will only render an empty div placeholder for the component</param>
    /// <param name="renderServerOnly">Render server only will not render the json props in hidden inputs</param>
    /// <param name="tagName"></param>
    /// <param name="cssClass"></param>
    /// <param name="id"></param>
    /// <param name="componentFullName">Specify the full component name if you do not use the default naming conventions</param>
    /// <param name="printNullValues">Print properties that are null, or skip printing them, you will then receive undefined</param>
    /// <example>
    /// <code>
    /// return await ReactServerSideResultAsync(currentBlock, new {
    ///     ContentAreaProperty = currentBlock.ContentAreaProperty.SelectFiltered&lt;ButtonBlock&gt;(),
    /// },
    /// : true);
    /// </code>
    /// </example>
    protected async Task<IViewComponentResult> ReactServerSideResultAsync(object model, object additionalProps = null, bool camelCaseProps = false, bool renderClientOnly = false, bool renderServerOnly = false, string tagName = "div", string cssClass = null, string id = null, string componentFullName = null, bool printNullValues = true)
    {
        return await Task.FromResult(ReactServerSideResult(model, additionalProps, camelCaseProps, renderClientOnly, renderServerOnly, tagName, cssClass, id, componentFullName, printNullValues));
    }

    /// <summary>
    /// Returns a react rendered view component
    /// <para>Assumes no cshtml files as it the engine returns HTML generated by the react component</para>
    /// </summary>
    /// <remarks>
    /// Assumes by default that all frontend react components is registered under DOM variable "window.reactComponents."
    /// </remarks>
    protected IViewComponentResult ReactServerSideResult(object model, object additionalProps = null, bool camelCaseProps = false, bool renderClientOnly = false, bool renderServerOnly = false, string tagName = "div", string cssClass = null, string id = null, string componentFullName = null, bool printNullValues = true)
    {
        var data = model.ReactServerSideRender(additionalProps, tagName, camelCaseProps, cssClass, id, componentFullName, renderClientOnly, renderServerOnly, printNullValues);

        return new HtmlContentViewComponentResult(new HtmlString(data.ToString()));
    }
}
